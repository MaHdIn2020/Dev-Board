document.getElementById('complete-1').addEventListener('click',function(event){
    event.preventDefault()
    alert("Board updated Successfully")
    document.getElementById('complete-1').disabled = true;
    let totalTaskcompleted = document.getElementById('total-task').innerText
    let taskAssigned = document.getElementById('task-assigned').innerText
    totalTaskcompleted = parseInt(totalTaskcompleted)
    taskAssigned = parseInt(taskAssigned)
    console.log(totalTaskcompleted,taskAssigned,typeof totalTaskcompleted, typeof taskAssigned)
    document.getElementById('total-task').innerText = totalTaskcompleted + 1
    document.getElementById('task-assigned').innerText = taskAssigned - 1

    if (taskAssigned -1 == 0){
        alert("Congrats!!! You have completed all the current tasks");
    }

    const heading = document.getElementById('name-1').innerText
    const activity = document.getElementById('activity')
    const paragraph = document.createElement('p')
    const time = currentTime()

    paragraph.innerText = `You have completed the task ${heading} at ${time}`
    paragraph.classList.add(
        "text-xl",
        "font-semibold",
        "bg-[#F4F7FF]",
        "p-3",
        "m-3",
        "rounded-lg"
    );
    activity.appendChild(paragraph)

})

